# Making a Dictionary

instructor = {
    "name": "Colt",
    "owns_dog": True,
    "nums_courses": 4,
    "favorite_language": "Python",
    "is_hilarious": False,
    44: "My favorite number!"
}

print(instructor)

cat = {
    "name": "Blue",
    "age": 3.5,
    "isCute": True
}

print(cat)

# Dict function

cat2 = dict(color = "Brown", age = 0.5)

print(cat2)

# Accessing individual values

print(cat["name"])

property = "isCute"

print(cat[property])